#include<bits/stdc++.h>
#define ll long long
using namespace std;




void solve() {
    ll n; cin>>n;
    ll m; cin>>m;
    for(ll i=1; i<=m; i++){
        cout<<n<<" x "<<i<<" = "<<n*i<<endl;
    }





}






int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int T = 1;
    while(T--) {
        solve();
    }
    return 0;
}

